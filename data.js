function loadGameState() {
    const saved = localStorage.getItem('englishLearningState_v2');
    if (saved) {
        gameState = JSON.parse(saved);
    }
    checkDailyLogin();
}

function saveGameState() {
    localStorage.setItem('englishLearningState_v2', JSON.stringify(gameState));
}

function checkDailyLogin() {
    const today = new Date().toDateString();
    if (gameState.lastLoginDate !== today) {
        if (gameState.lastLoginDate) {
            const yesterday = new Date();
            yesterday.setDate(yesterday.getDate() - 1);
            if (gameState.lastLoginDate === yesterday.toDateString()) {
                gameState.streak++;
            } else {
                gameState.streak = 1;
            }
        } else {
            gameState.streak = 1;
        }
        gameState.todayLearned = 0;
        gameState.lastLoginDate = today;
        addScore(10);
        saveGameState();
    }
}

function addScore(amount) {
    gameState.score += amount;
    saveGameState();
    updateScoreDisplay();
}

function unlockAchievement(id) {
    if (!gameState.achievements.includes(id)) {
        gameState.achievements.push(id);
        if (!gameState.achievementTimes) gameState.achievementTimes = {};
        gameState.achievementTimes[id] = new Date().toLocaleString('zh-CN');
        saveGameState();
        const ach = achievements.find(a => a.id === id);
        if (ach) {
            showAchievementToast(ach);
        }
        if (typeof displayAchievementsRedesigned === 'function') {
            displayAchievementsRedesigned();
        }
        if (typeof updateAchievementStats === 'function') {
            updateAchievementStats();
        }
    }
}

function showAchievementToast(ach) {
    const toast = document.getElementById('achievement-toast');
    const toastName = document.getElementById('achievement-toast-name');
    if (toast && toastName) {
        toastName.textContent = ach.name;
        toast.classList.remove('hidden');
        toast.classList.add('show');
        setTimeout(() => {
            toast.classList.remove('show');
            toast.classList.add('hidden');
        }, 4000);
    }
}

function updateScoreDisplay() {
    const scoreDisplay = document.getElementById('score-display');
    const gachaScore = document.getElementById('gacha-score');
    if (scoreDisplay) scoreDisplay.textContent = gameState.score;
    if (gachaScore) gachaScore.textContent = gameState.score;
}

function generateCardRarity() {
    const rand = Math.random();
    let cumulative = 0;
    for (const [rarity, weight] of Object.entries(rarityWeights)) {
        cumulative += weight;
        if (rand < cumulative) return rarity;
    }
    return 'common';
}

